#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	pos;

	if (!s)
		return (NULL);
	str = (char *)ft_calloc(ft_strlen(s) + 1, sizeof(char));
	if (!str)
		return (NULL);
	pos = 0;
	while (s[pos] != '\0')
	{
		str[pos] = f(pos, s[pos]);
		pos++;
	}
	return (str);
}
